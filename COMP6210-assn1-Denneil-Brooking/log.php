                    <p class="lead text-muted">
                        Server-side scripting is a technique used in web development which
                        involves employing scripts on a web server which produce a response customized for each user's
                        (client's) request to the website. The alternative is for the web server itself to deliver a
                        static web page.
                        <br></p>
