                        <Code>/</Code> <Code>%</Code> <Code>**</Code>
                        <br><br>
                        <u>Assignment operators:</u> The PHP assignment
                        operators are used with numeric values to write a value to a variable.
                        The basic assignment operator in PHP is "=". It means that the left operand gets set to the
                        value of the assignment expression on the right. <br> <Code>x=y</Code> <Code>x+=y</Code> <Code>x-=y</Code>
                        <Code>x*=y</Code> <Code>x/=y</Code> <Code>x%=y</Code>
                        <br><br>
                        <u>Comparison operators:</u> The PHP comparison
                        operators are used to compare two values (number or string) <br> <Code>==</Code> <Code>===</Code>
                        <Code>!=</Code> <Code>&lt;></Code> <Code>!==</Code> <Code>></Code> <Code>&lt;</Code> <Code>>=</Code>
                        <Code>&lt;=</Code>
                        <br><br>
                        <u>Increment/Decrement operators:</u> The PHP
                        increment operators are used to increment a variable's value.<br>
                        The PHP decrement operators are used to decrement a variable's value. <br> <Code>++$x</Code>
                        <Code>$x++</Code> <Code>--$x</Code> <Code>$x--</Code>
                        <br><br>
                        <u>Logical operators:</u> The PHP logical
                        operators are used to combine conditional statements. <br> <Code>and</Code> <Code>or</Code>
                        <Code>xor</Code> <Code>&&</Code> <Code>||</Code> <Code>!</Code>
                        <br><br>
                        <u>String operators:</u> PHP has two operators
