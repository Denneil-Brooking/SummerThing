                    <h4>PHP</h4>
                    <p class="lead text-muted"><u>What is PHP: </u> Server-side scripting language designed for Web
                        development
                        <br>
                        <br><u>How it works:</u>The PHP software works with the web server, which is the software that
                        delivers web pages to the world. When you type a URL into your web browser’s address bar,
                        you’re sending a message to the web server at that URL, asking it to send you an HTML file. The
                        web server responds by sending the requested file. Your browser reads the HTML file and
                        displays the web page.
                        <br></p>
