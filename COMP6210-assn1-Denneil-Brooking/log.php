                    <h4>Conditionals</h4>
                    <p class="lead text-muted">
                        <u>if statement:</u> executes some code if one condition is true
                        <br><Code>if (condition) {
                                    <br>code to be executed if condition is true;
                                    <br>} </Code>
                        <br><br>
                        <u>if-else statement:</u> executes some code if a condition is true and another code if that
                        condition is false
                        <br><code>if (condition) {
                                 <br>   code to be executed if condition is true;
                                 <br>} else {
                                    <br>code to be executed if condition is false;
                                    <br>} </code>
                        <br><br>
                        <u>if-else-if-else statement:</u> executes different codes for more than two conditions
                        <br><code>if (condition) {
                                    <br>    code to be executed if this condition is true;
                                    <br>} elseif (condition) {
                                        <br>    code to be executed if this condition is true;
                                        <br> } else {
                                            <br>     code to be executed if all conditions are false;
                                            <br> } </code>
                        <br><br>
                        <u>Switch statement:</u> selects one of many blocks of code to be executed
                        <br><code>switch (n) {
                                    <br>    case label1:
                                    <br>code to be executed if n=label1;
                                    <br>break;
                                    <br>case label2:
                                    <br>code to be executed if n=label2;
                                    <br>break;
                                    <br>case label3:
                                    <br>code to be executed if n=label3;
                                    <br>break;
                                    <br>...
                                    <br>default:
                                    <br>code to be executed if n is different from all labels;
                                    <br>  } </code>
                    </p>
