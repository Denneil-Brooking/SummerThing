                                <br>    code to be executed;
                                <br>} </code>
                        <br><br>
                        <u>DoWhile:</u> loops through a block of code once, and then repeats the loop as long as the
                        specified condition is true
                        <br><code>do {
                                <br>    code to be executed;
                                <br>} while (condition is true); </code>
                        <br><br>
                        <u>For:</u> loops through a block of code a specified number of times
                        <br><code>for (init counter; test counter; increment counter) {
                                <br>    code to be executed;
                                <br>} </code>
                        <br><br>
                        <u>ForEach:</u> loops through a block of code for each element in an array
                        <br><code>foreach ($array as $value) {
                                <br>    code to be executed;
                                <br>} </code>
                    </p><br>
